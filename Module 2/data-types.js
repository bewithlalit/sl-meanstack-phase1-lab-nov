// number
var x = 10;
console.log(typeof x);
console.log(x);

// string
var myString = "my own string";
console.log(myString);
console.log(typeof myString);

// Array
var cars = ["Honda", 123, "BMW"]
console.log(cars);
console.log(typeof cars);

// Object
var obj = {name: 'John', age: 21};
console.log(obj);
console.log(typeof obj);

// undefined
var y;
console.log(y);
console.log(typeof y);

// null
var n = null;
console.log(n);
console.log(typeof n);

